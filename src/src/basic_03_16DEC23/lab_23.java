package dec_16_23;

public class lab_23 {
    public static void main(String[] args) {

        // || - OR
        // T || T -> T
        // T || F -> T
        // F || T -> T
        // F || F -> F


        System.out.println(true || true);
        System.out.println(true || false);
        System.out.println(false || true);
        System.out.println(false || false);
        System.out.println();


        //  And  &&

        // T || T -> T
        // T || F -> F
        // F || T -> F
        // F || F -> F

        System.out.println(true && true);
        System.out.println(true && false);
        System.out.println(false && true);
        System.out.println(false && false);






    }
}
